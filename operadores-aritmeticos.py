"""
    Uso de operadores aritméticos
"""

a = 20
b = 4

print("Suma:", a + b)         # 13
print("Resta:", a - b)        # 7
print("Multiplicación:", a * b) # 30
print("División real:", a / b)  # 3.3333...
print("División entera:", a // b) # 3
print("Residuo:", a % b)       # 1
print("Potenciación:", a ** b) # 1000 (10^3)
